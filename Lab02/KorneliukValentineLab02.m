x = [5, 9.5, 11, 13, 15, 18, 22, 26, 29];
y = [0.5, 0.7, 1, 1.4, 1.6, 2, 2.4, 3, 3];
plot(x, y);

A = [10, sum(x), sum(x.^2); sum(x), sum(x.^2), sum(x.^3); sum(x.^2), sum(x.^3), sum(x.^4)];
B = [sum(y); x*y'; x.^2 * y'];
C = A \ B;

y1 = C(1) + C(2) * x + C(3) * (x .^2);

hold on
plot(x, y1);
hold off
legend('Исходная функция', 'Аппроксимирующая функция');